int		ft_nbrlen(int n)
{
	int					len;
	unsigned int		nb;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	nb = n;
	while (nb >= 10)
	{
		len++;
		nb /= 10;
	}
	len++;
	return (len);
}
